package com.app.MavenSpringBootMvcFormHandlingWithHandlerInterceptorsThruAnnotation.model;

import javax.validation.constraints.NotBlank;
import org.springframework.stereotype.Component;

@Component
public class Student {
	
	@NotBlank
	private int id;
	
	@NotBlank
	private String name;
	
	@NotBlank
	private int age;
	
	@NotBlank
	private String city;

	/**
	 * @return the id
	 */
	public int getId() {
		return id;
	}

	/**
	 * @param id the id to set
	 */
	public void setId(int id) {
		this.id = id;
	}

	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}

	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}

	/**
	 * @return the age
	 */
	public int getAge() {
		return age;
	}

	/**
	 * @param age the age to set
	 */
	public void setAge(int age) {
		this.age = age;
	}

	/**
	 * @return the city
	 */
	public String getCity() {
		return city;
	}

	/**
	 * @param city the city to set
	 */
	public void setCity(String city) {
		this.city = city;
	}
}
